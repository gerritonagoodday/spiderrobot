void
ep_line(unsigned char* frame, int x, int y, int x2, int y2, unsigned char col, char weight)
{
    int dx, dy;
    int sx, sy, err, e2;
    
    if (x>x2)
        dx=x-x2;
    else
        dx=x2-x;
    if (y>y2)
        dy=y-y2;
    else
        dy=y2-y;

    if (x<x2)
        sx=1;
    else
        sx=-1;
  
    if (y<y2)
        sy=1;
    else
        sy=-1;
        
    err=dx-dy;
 
    while(1)
    {
        ep_plot(frame, x, y, col);
        switch(weight)
        {
            case PEN_THIN:
                break;
            case PEN_MED:
                if (EP_HEIGHT-1>=x+1)
                    ep_plot(frame, x+1, y, col);
                if (0<=x-1)
                    ep_plot(frame, x-1, y, col);
                if (EP_WIDTH-1>=y+1)
                    ep_plot(frame, x, y+1, col);
                if (0<=y-1)
                    ep_plot(frame, x, y-1, col);
                break;
            case PEN_THICK:
                ep_blob(frame, x, y, col);
                break;
            default:
                break;
        }
        if ((x==x2) && (y==y2))
            break;
        e2=2*err;
        if (e2>-dy)
        {
            err=err-dy;
            x=x+/*(unsigned char)*/sx;
        }
        if ((x==x2) && (y==y2))
        { 
            ep_plot(frame, x, y, col);
            switch(weight)
            {
                case PEN_THIN:
                    break;
                case PEN_MED:
                    if (EP_HEIGHT-1>=x+1)
                        ep_plot(frame, x+1, y, col);
                    if (0<=x-1)
                        ep_plot(frame, x-1, y, col);
                    if (EP_WIDTH-1>=y+1)
                        ep_plot(frame, x, y+1, col);
                    if (0<=y-1)
                        ep_plot(frame, x, y-1, col);
                    break;
                case PEN_THICK:
                    ep_blob(frame, x, y, col);
                    break;
                default:
                    break;
            }
            break;
        }
        if (e2<dx)
        { 
            err=err+dx;
            y=y+/*(unsigned char)*/sy; 
        }
    } // end while(1)
    
}

void
ep_blob(unsigned char* frame, int x, int y, unsigned char col)
{
    ep_line(frame, x-1,y+2,  x+1,y+2, col);
    ep_line(frame, x-2,y+1,  x+2,y+1, col);
    ep_line(frame, x-2,y,  x+2,y, col);
    ep_line(frame, x-2,y-1,  x+2,y-1, col);
    ep_line(frame, x-1,y-2,  x+1,y-2, col);
}

int
ep_write_text(unsigned char* frame, char* text, int x, int y, unsigned char col,
                double xscale, double yscale, unsigned int* dy, char weight)
{
    int xx, mdy;
    int wt;
    int i, im;
    int ci=0;
    unsigned char c;
    int nv;
    int /*int8_t*/ w;
    int ipx, ipy;
    int px, py;
    int ox, oy;
    int old=0;
    int do_scale=0;
    int not_finished=1;
    mdy=0;
    
    ox=0; oy=0;
    wt=0;
    
    if ((xscale>1.01) || (xscale<1.01) || (yscale>1.01) || (yscale<1.01))
        do_scale=1;
    
    xx=x;
    while(not_finished)
    {
        c=text[ci];
        ci++;
        if (c=='\0')
            break;
        if ((c>126) || (c<32))
            continue;
        c=c-32;
        nv=simplex[c][0];
        w=simplex[c][1];
        if (w<0)
            w=0-w;
        if (nv==0)
        {
            xx=xx+w;
            wt=wt+w;
            continue;
        }
        im=2+(nv*2);
        old=0;
        for (i=2; i<im; i=i+2)
        {
            ipx=simplex[c][i];
            ipy=simplex[c][i+1];
            if (ipx>=0)
            {
                px=ipx;
            }
            else
            {
                px=0-ipx;
            }
            if (ipy>=0)
            {
                py=ipy;
            }
            else
            {
                if (py==-1)
                {
                    py=32767;
                }
                else
                {
                    py=0-ipy;
                }
            }
            
            if (do_scale)
            {
                //px=(unsigned int)(((double)ipx)*xscale);
                //py=(unsigned int)(((double)ipy)*yscale);
                px=((double)px)*xscale;
                py=((double)py)*yscale;
            }
            if ((py!=32767) && (py>mdy))
                mdy=py+1;
            if ((ipx==-1) && (ipy==-1)) // pen up
            {
                old=0;
            }
            else
            {
                if (old) // a previous point is stored in ox, oy
                {
                    if (((ox+xx)>(EP_HEIGHT-1)) || ((px+xx)>(EP_HEIGHT-1)))
                    {
                        not_finished=0;
                        break;
                    }
                    ep_line(frame, ox+xx, oy+y, px+xx, py+y, col, weight);
                    ox=px; oy=py;
                }
                else
                {
                    ox=px; oy=py;
                    old=1;
                }
            }
        }
        if (do_scale)
        {
            xx=xx+(unsigned int)(((double)w)*xscale);
            wt=wt+(unsigned int)(((double)w)*xscale);
        }
        else
        {
            xx=xx+w;
            wt=wt+w;
        }
    }

    if (dy!=NULL)
        *dy=mdy;
    return(wt+1);
}

